function onCreatureSay(cid, type, msg)
	msg = string.lower(msg) 
	dist = getDistanceToCreature(cid)
	if dist >= 8 then
	return
end

talk_start = os.clock() 

if string.find(msg, 'hi') and string.len(msg) == 2 then
	if focus == cid then
	selfSay('I am talking to you...')
	else if focus > 0 then
        selfSay('Wait please.')
        end
	end
	if(focus == 0) then
	selfSay('Hail, I am Camehlia.')
	focus = cid
	end
	talk_start = os.clock()
end

if string.find(msg, '(%a*)offer(%a*)') and cid == focus then
	selfSay('I used to sell stuff for house, but house system is not avaiable yet ;/')
end

if string.find(msg, '(%a*)job(%a*)') and cid == focus then
	selfSay('I used to sell stuff for house, but house system is not avaiable yet ;/')
end

if string.find(msg, 'bye(%a*)') and cid == focus then
	selfSay('Farewell, ' .. creatureGetName(cid) .. '!')
	focus = 0
	itemid = 0
	talk_start = 0
	talkcount = 0
end 

end

